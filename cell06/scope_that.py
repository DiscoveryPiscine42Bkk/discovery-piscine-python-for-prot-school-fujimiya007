def add_one(num):
  """
  ฟังก์ชันที่รับพารามิเตอร์หนึ่งตัวและบวกค่า 1 เข้ากับพารามิเตอร์นั้น
  """
  num += 1
  return num

# ส่วนหลักของโปรแกรม
my_variable = 10
print("ค่าของตัวแปรก่อนเรียกใช้ฟังก์ชัน:", my_variable)

add_one(my_variable) # เรียกใช้ฟังก์ชัน add_one โดยส่งค่า my_variable ไป
print("ค่าของตัวแปรหลังเรียกใช้ฟังก์ชัน:", my_variable)

# สังเกตผลลัพธ์
print("\nสิ่งที่สังเกตได้:")
print("ค่าของตัวแปร my_variable ไม่เปลี่ยนแปลงหลังจากการเรียกใช้ฟังก์ชัน add_one")
print("เนื่องจากฟังก์ชัน add_one ทำงานกับสำเนาของตัวแปร my_variable")
print("ไม่ใช่ตัวแปร my_variable ตัวจริงในส่วนหลักของโปรแกรม")